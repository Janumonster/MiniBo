package com.zzy.minibo.Members;

public class MySize {

    private int width;
    private int height;
    private boolean isLongPic = false;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public boolean isLongPic() {
        return isLongPic;
    }

    public void setLongPic(boolean longPic) {
        isLongPic = longPic;
    }
}
